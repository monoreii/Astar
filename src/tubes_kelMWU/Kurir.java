    private void updateShipmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateShipmentActionPerformed
        // TODO add your handling code here:
        int no_resi = Integer.parseInt(Fresi.getText());
        int biayaPengiriman = Integer.parseInt(Fbiayapengiriman.getText());
        String tanggalPengiriman = Ftanggalkirim.getText();
        String tanggalSampai = Ftanggalsampai.getText();
        String status = "Terkirim";

        String sql = "UPDATE shipment SET tanggal_sampai='"+tanggalSampai+"',status='"+status+"' WHERE no_resi='"+no_resi+"'";
        try{
            Connection conn = koneksi.getConnection();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil di-update di database");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        load_table();
    }//GEN-LAST:event_updateShipmentActionPerformed


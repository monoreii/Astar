    public void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("No Resi");
        model.addColumn("ID paket");
        model.addColumn("Nama penerima");
        model.addColumn("NoHP penerima");
        model.addColumn("Alamat penerima");
        model.addColumn("Biaya Pengiriman");
        model.addColumn("Jenis service");
        model.addColumn("Tanggal Pengiriman");
        model.addColumn("Tanggal Sampai");
        model.addColumn("Status");
        try {
            int no=1;
            String sql = "SELECT shipment.* ,order_paket.*, service.* FROM shipment "
                    + "JOIN order_paket ON (shipment.id_paket = order_paket.id_paket)"
                    + "JOIN service ON (order_paket.id_service = service.id_service)";
            Statement stm = (Statement) koneksi.getConnection().createStatement();
            ResultSet res = stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString("no_resi"),res.getString("id_paket"),res.getString("nama_penerima"),res.getString("noHp_penerima"),res.getString("alamat_tujuan"),res.getString("biaya_pengiriman"),res.getString("title_service"),res.getString("tanggal_pengiriman"),res.getString("tanggal_sampai"),res.getString("status")});
            }
            table_shipment.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    

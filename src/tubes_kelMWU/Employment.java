    private void BtambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtambahActionPerformed
        // TODO add your handling code here: 
        String nama = Fnama_peg.getText();
        String jabatan = Cjabatan_peg.getSelectedItem().toString();
        String noTelp= Fnotelp_peg.getText();
        String alamat= Talamat_peg.getText();
        String username= Fusername_peg.getText() ;
        String password= Fpassword_peg.getText() ;
        if(pengecekan()){
            try {
                String sql = "INSERT INTO employee VALUES ('"+0+"','"+nama+"','"+jabatan+"','"+noTelp+"','"+alamat+"','"+username+"','"+password+"')";
                Connection conn= koneksi.getConnection();
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
            }catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            load_table();
            kosong();
        }else{
            JOptionPane.showMessageDialog(null, "Semua data harus di isi");
        }
        
        
    }//GEN-LAST:event_BtambahActionPerformed

    private void BeditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BeditActionPerformed
        // TODO add your handling code here:
        String id_peg = Fid_pegawai.getText();
        String nama = Fnama_peg.getText();
        String jabatan = Cjabatan_peg.getSelectedItem().toString();
        String noTelp= Fnotelp_peg.getText();
        String alamat= Talamat_peg.getText();
        String username= Fusername_peg.getText() ;
        String password= Fpassword_peg.getText() ;
        if(pengecekan() && !"".equals(id_peg)){
           String sql = "UPDATE employee SET id_pegawai='"+id_peg+"',Nama_peg='"+nama+"',Jabatan_peg='"+jabatan+"',NoTelp_peg='"+noTelp+"',Alamat_peg='"+alamat+"',Username_peg='"+username+"',Password_peg='"+password+"' WHERE id_pegawai='"+id_peg+"'";
            try{
                Connection conn = koneksi.getConnection();
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data berhasil di-update di database");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            load_table();
            kosong(); 
        }else{
            JOptionPane.showMessageDialog(null, "Semua data harus di isi");
        }
    }//GEN-LAST:event_BeditActionPerformed

    private void BhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BhapusActionPerformed
        // TODO add your handling code here:
        if(!"".equals(Fid_pegawai.getText())){
            String sql = "DELETE FROM employee WHERE id_pegawai='"+Fid_pegawai.getText()+"'";
            try{
                Connection conn = koneksi.getConnection();
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus di database");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            load_table();
            kosong();
        }else{
            JOptionPane.showMessageDialog(null, "Pilih data yang mau dihapus!!");
        }
        
    }//GEN-LAST:event_BhapusActionPerformed

